"""
Общие утилиты для стохастических моделей факторов (этап 3).

Все модели подгоняются методом максимального правдоподобия (MLE) и
сравниваются по информационным критериям. Так как у моделей разное число
параметров (нормаль vs t-Стьюдента с дополнительным ν, GARCH с динамикой
дисперсии), честное сравнение — именно по AIC/BIC, а не по «сырому» log-lik.
"""
from __future__ import annotations

import numpy as np


def aic(loglik: float, n_params: int) -> float:
    """Информационный критерий Акаике: меньше — лучше."""
    return 2 * n_params - 2 * loglik


def bic(loglik: float, n_params: int, n_obs: int) -> float:
    """Байесовский ИК (сильнее штрафует параметры): меньше — лучше."""
    return n_params * np.log(n_obs) - 2 * loglik


def n_cov_params(p: int) -> int:
    """Число свободных параметров симметричной p×p матрицы (μ не считаем)."""
    return p * (p + 1) // 2
