RUB = 1.0

BOND_NOTIONAL_EACH: float = 10_000_000.0   # 10 млн руб. в каждую облигацию
EQUITY_NOTIONAL_EACH: float = 1_000_000.0  # 1 млн руб. в каждую акцию
FX_NOTIONAL_EACH: float = 100_000_000.0    # 100 млн руб. в USD и столько же в EUR


@dataclass(frozen=True)
class PortfolioSpec:
    """Целевые РУБЛЁВЫЕ веса портфеля. Единицы инструментов пересчитываются
    ежедневно по ценам предыдущего дня (см. условие п.5c — ребалансировка)."""

    # Стартовая «лесенка» по кривой (короткий→длинный), все с историей с 2021 г.
    # Подтвердить MATDATE через scripts/explore_universe.py.
    bonds: tuple[str, ...] = (
        "SU26226RMFS9",  # 26226, погашение ~окт 2026  (короткий конец)
        "SU26224RMFS4",  # 26224, погашение ~май 2029  (средний)
        "SU26218RMFS6",  # 26218, погашение ~сен 2031  (средне-длинный)
        "SU26225RMFS1",  # 26225, погашение ~май 2034  (длинный)
        "SU26238RMFS4",  # 26238, погашение 15.05.2041 (сверхдлинный)
    )
    stocks: tuple[str, ...] = (
        "SBER", "GAZP", "LKOH", "GMKN", "ROSN",
        "NVTK", "MGNT", "MTSS", "YDEX", "PLZL",
    )
    fx: tuple[str, ...] = ("USD", "EUR")

    bond_notional: float = BOND_NOTIONAL_EACH
    equity_notional: float = EQUITY_NOTIONAL_EACH
    fx_notional: float = FX_NOTIONAL_EACH

    def total_value(self) -> float:
        return (
            len(self.bonds) * self.bond_notional
            + len(self.stocks) * self.equity_notional
            + len(self.fx) * self.fx_notional
        )


PORTFOLIO = PortfolioSpec()

